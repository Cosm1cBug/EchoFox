'use strict';

/**
 * Auto-generate docs/commands.md from src/commands/<category>/<name>.js
 * metadata. Idempotent. Run via `npm run docs:commands`.
 *
 *   • Skips _disabled/ and __tests__/.
 *   • Soft-fails on individual file errors (logs and continues).
 *   • Output is sorted: category alphabetic, command alphabetic within.
 */
const fs   = require('node:fs');
const path = require('node:path');

const ROOT    = path.join(__dirname, '..', 'src', 'commands');
const OUTPUT  = path.join(__dirname, '..', 'docs', 'commands.md');
const EXCLUDE = new Set(['__tests__', '_disabled']);


/**
 * Extract command metadata from source WITHOUT executing it.
 * We can't safely require() commands that import optional deps
 * (axios, ytdl-core, etc.) just to read their name + desc.
 */
function staticMeta(src) {
  const pick = (key) => {
    const re = new RegExp(`['"\`]?${key}['"\`]?\\s*:\\s*(['"\`])([^\\1]*?)\\1`);
    const m = src.match(re);
    return m ? m[2] : '';
  };
  const pickBool = (key) => {
    const m = src.match(new RegExp(`['"\`]?${key}['"\`]?\\s*:\\s*(true|false)`));
    return m ? m[1] === 'true' : false;
  };
  const pickNum = (key) => {
    const m = src.match(new RegExp(`['"\`]?${key}['"\`]?\\s*:\\s*(\\d+)`));
    return m ? Number(m[1]) : 0;
  };
  const pickArray = (key) => {
    const m = src.match(new RegExp(`['"\`]?${key}['"\`]?\\s*:\\s*\\[([^\\]]*)\\]`));
    if (!m) return [];
    return [...m[1].matchAll(/['"`]([^'"`]+)['"`]/g)].map((mm) => mm[1]);
  };
  return {
    name:     pick('name'),
    desc:     pick('desc'),
    alias:    pickArray('alias'),
    requires: pickArray('requires'),
    admin:    pickBool('admin'),
    group:    pickBool('group'),
    cooldown: pickNum('cooldown'),
    hasStart: /(?:['"`]?start['"`]?\s*:\s*(?:async)?\s*\(|async\s+start\s*\()/.test(src),
  };
}

function loadCommandsByCategory() {
  const byCat = {};
  const cats = fs.readdirSync(ROOT, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !EXCLUDE.has(d.name) && !d.name.startsWith('.'))
    .map((d) => d.name)
    .sort();

  for (const cat of cats) {
    byCat[cat] = [];
    const dir = path.join(ROOT, cat);
    const files = fs.readdirSync(dir).filter((f) => f.endsWith('.js')).sort();

    for (const file of files) {
      const full = path.join(dir, file);
      try {
        // Static parse to avoid executing top-level requires of optional deps.
        const src = fs.readFileSync(full, 'utf8');
        const mod = staticMeta(src);
        if (!mod.name || !mod.hasStart) {
          console.warn(`⚠️  skipped (malformed): ${cat}/${file}`);
          continue;
        }
        byCat[cat].push({
          name:     mod.name,
          alias:    mod.alias,
          desc:     mod.desc,
          admin:    mod.admin,
          group:    mod.group,
          requires: mod.requires,
          cooldown: mod.cooldown,
          file:     `src/commands/${cat}/${file}`,
        });
      } catch (e) {
        console.warn(`⚠️  parse error in ${cat}/${file}: ${e.message}`);
      }
    }
    if (!byCat[cat].length) delete byCat[cat];
  }
  return byCat;
}

function badges(c) {
  const b = [];
  if (c.admin)         b.push('🔒 admin');
  if (c.group)         b.push('👥 group');
  if (c.requires.length) b.push(`🔑 needs \`${c.requires.join('`, `')}\``);
  if (c.cooldown > 0)  b.push(`⏱️ ${c.cooldown}s cooldown`);
  return b.length ? ` _${b.join(' · ')}_` : '';
}

function render(byCat) {
  const total = Object.values(byCat).reduce((a, c) => a + c.length, 0);
  const cats  = Object.keys(byCat);

  let md = `# EchoFox Command Catalog\n\n`;
  md += `> Auto-generated by \`scripts/generate-command-docs.js\` — do not edit by hand.\n`;
  md += `> Re-run with \`npm run docs:commands\` after adding or modifying commands.\n\n`;
  md += `**${total}** commands across **${cats.length}** categories.\n\n`;

  // TOC
  md += `## Table of contents\n\n`;
  for (const cat of cats) {
    md += `- [${cat}](#${cat}) — ${byCat[cat].length}\n`;
  }
  md += `\n`;

  // Per-category sections
  for (const cat of cats) {
    md += `## ${cat}\n\n`;
    md += `| Command | Aliases | Description |\n|---|---|---|\n`;
    for (const c of byCat[cat]) {
      const aliases = c.alias.length ? c.alias.map((a) => `\`.${a}\``).join(', ') : '—';
      md += `| **\`.${c.name}\`** | ${aliases} | ${c.desc || '_(undocumented)_'}${badges(c)} |\n`;
    }
    md += `\n`;
  }

  md += `---\n\n`;
  md += `## Legend\n\n`;
  md += `- 🔒 **admin** — only users in \`config.admins\` can run this command\n`;
  md += `- 👥 **group** — only works inside group chats\n`;
  md += `- 🔑 **needs** — auto-disabled at startup if listed config paths are empty\n`;
  md += `- ⏱️ **cooldown** — per-user delay between consecutive uses\n`;

  return md;
}

function main() {
  const byCat = loadCommandsByCategory();
  const md    = render(byCat);
  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, md);
  const total = Object.values(byCat).reduce((a, c) => a + c.length, 0);
  console.log(`✅ wrote ${OUTPUT} (${total} commands, ${Object.keys(byCat).length} categories)`);
}

if (require.main === module) main();
